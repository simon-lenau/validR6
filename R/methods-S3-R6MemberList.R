# ================================= > `[` < ================================== #
#' @export
`[.R6MemberList` <-
    function(x, i, ...) {
        x$select(i, inner = FALSE)
    }
# ────────────────────────────────── <end> ─────────────────────────────────── #

# ================================ > `[<-` < ================================= #
#' @export
`[<-.R6MemberList` <-
    function(x, i, value) {
        x$select(i, new_value = value, inner = FALSE)
        return(x)
    }
# ────────────────────────────────── <end> ─────────────────────────────────── #

# ================================= > `[[` < ================================= #
#' @export
`[[.R6MemberList` <-
    function(x, i, ...) {
        x$select(i, inner = TRUE)
    }
# ────────────────────────────────── <end> ─────────────────────────────────── #

# ================================ > `[[<-` < ================================ #
#' @export
`[[<-.R6MemberList` <-
    function(x, i, value) {
        x$select(i, new_value = value, inner = TRUE)
        return(x)
    }
# ────────────────────────────────── <end> ─────────────────────────────────── #
